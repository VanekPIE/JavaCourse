package Lesson_06_with_disks;

public class Disks {
    private  String name;
    private  int year;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public Disks(String name, int year) {
        this.name = name;
        this.year = year;



    }
}
